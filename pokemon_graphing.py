def pokemon_num(month, day, name_length):
    result_num = month * day * name_length
    while result_num > 649:
        result_num /= 2
    return int(result_num // 1)

def main():
    frequencies = dict([(name_len, [0]*649) for name_len in range(1, 317+1)])
    total_frequencies = [0]*649

    for month, num_days in [(1, 31), (2, 29), (3, 31), (4, 30), (5, 31), (6, 30),
            (7, 31), (8, 31), (9, 30), (10, 31), (11, 30), (12, 31)]:
        for day in range(1, num_days+1):
            for name_length in frequencies:
                offset_pokenum = pokemon_num(month, day, name_length) - 1
                frequencies[name_length][offset_pokenum] += 1
                total_frequencies[offset_pokenum] += 1

    for name_length in frequencies:
        print("Name length {0}:".format(name_length))
        for offset_pokenum in range(649):
            if frequencies[name_length][offset_pokenum] > 0:
                padded_num = "0"*(3-len(str(offset_pokenum+1))) + str(offset_pokenum+1)
                print("{0}: {1}".format(padded_num,
                    "#"*frequencies[name_length][offset_pokenum]))
        print("Pokemon that never came up for length {0}: {1}".format(name_length,
            ", ".join(pokemon_names[n] for n in range(649) if frequencies[name_length][n] == 0)))
        print("")

    print("Pokemon that never came up: {0}".format(", ".join(pokemon_names[n] for n in
        range(649) if total_frequencies[n] == 0)))

    print("")

    total_frequencies_dict = dict((n, total_frequencies[n]) for n in range(649))
    print("Pokemon that came up from most to least common:")
    for n, freq in reversed(sorted(total_frequencies_dict.items(), key=itemgetter(1))):
        if freq > 0:
            print("{0}: {1}".format(pokemon_names[n], freq))

if __name__ == '__main__':
    main()
